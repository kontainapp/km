/*
 * First memort regions are: 2mb - 4mb, 4mb - 8mb, 8mb - 16mb ...
 * We place brk to 9mb, or into the third region. Then we initialize memory around 8mb boundary, and
 * print it to make sure all of the content makes it.
 */
#define BUFSIZE 16 * 1024
#define MiB 1024 * 1024
void* const top = (void*)(9 * MiB);
const long data[BUFSIZE / sizeof(long)];   // forward declaration - real data at the end of the file
char data_in[BUFSIZE];

void* SYS_break(void const* addr)
{
   return (void*)syscall(SYS_brk, addr);
}

static void inline IGUR()
{
} /* Ignore GCC Unused Result */

TEST region(void)
{
   int fd;
   void* ptr;

   int pid = getpid();
   char kmtest_data_path[PATH_MAX + 1];
   snprintf(kmtest_data_path, PATH_MAX + 1, "/tmp/kmtest_data%d", pid);

   if ((fd = open(kmtest_data_path, O_CREAT | O_TRUNC | O_RDWR, 0666)) < 0) {
      perror(kmtest_data_path);
   }
   if (SYS_break(top) != top) {
      fprintf(stderr, "break "); 
      perror(km_data_path);
   }
   ptr = top - MiB - 8000;   // one full page and one partial page below the region boundary
   memcpy(ptr, data, BUFSIZE);
   IGUR(write(fd, ptr, BUFSIZE));
   lseek(fd, SEEK_SET, 0);
   IGUR(read(fd, data_in, BUFSIZE));
   close(fd);
   unlink(kmtest_data_path);
   ASSERT_EQ(memcmp(data, data_in, BUFSIZE), 0);
   PASS();
}

GREATEST_MAIN_DEFS();
int main(int argc, char** argv)
{
   GREATEST_MAIN_BEGIN();
   greatest_set_verbosity(1);

   RUN_TEST(region);

   GREATEST_PRINT_REPORT();
   exit(greatest_info.failed);   // return count of errors (or 0 if all is good)
}

